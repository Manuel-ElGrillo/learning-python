# Tuplas en Python
# Son secuencias inmutables (no se pueden modificar)
# Se definen usando paréntesis ()

# Ejemplo básico de tupla

tupla_basica = (1, 2, 3)

#Pero también se puede usar sin paréntesis
tupla_sin_parentesis = 1, 2, 3

#Las tuplas pueden contener diferentes tipos de datos
tupla_mixta = ("Hola", 42, True)



